package cn.com.shanda.aesop.blog.bean;

public class FriendBean {
	private int id=-1;
	private String name="";
	private String blog="";	
	
	public String getBlog() {
		return blog;
	}
	public void setBlog(String blog) {
		this.blog = blog;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
